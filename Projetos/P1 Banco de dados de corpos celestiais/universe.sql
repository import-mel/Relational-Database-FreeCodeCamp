CREATE DATABASE universe;

\c universe;

CREATE TABLE galaxy(
    galaxy_id SERIAL PRIMARY KEY,
    galaxy_name VARCHAR(100) UNIQUE NOT NULL,
    galaxy_type TEXT NOT NULL,
    description_galaxy TEXT,
    distance_from_earth NUMERIC,
    diameter_kly NUMERIC(10, 2) NOT NULL,
    has_supernovae BOOLEAN,
    has_nebula BOOLEAN,
    mass_solar_galaxy NUMERIC(15, 2),
    age_billion INT,
    discovery_year_galaxy INT
);

CREATE TABLE star(
    star_id SERIAL PRIMARY KEY,
    galaxy_id INT NOT NULL,
    star_name VARCHAR(100) UNIQUE NOT NULL,
    star_type TEXT NOT NULL,
    spectral_type VARCHAR(10) NOT NULL,
    temperature_k NUMERIC(10, 2) NOT NULL,
    luminosity NUMERIC(10, 2),
    mass_solar NUMERIC(10, 2),
    main_sequence BOOLEAN NOT NULL,
    rotation_period_days NUMERIC(10, 2),
    CONSTRAINT fk_galaxy_id_star FOREIGN KEY (galaxy_id) REFERENCES galaxy(galaxy_id)
);

CREATE TABLE planet(
    planet_id SERIAL PRIMARY KEY,
    star_id INT NOT NULL,
    planet_name VARCHAR(100) NOT NULL UNIQUE,
    orbital_period_days NUMERIC(10, 2) NOT NULL,
    is_exoplanet BOOLEAN,
    radius_km NUMERIC(12, 2) NOT NULL,
    gravity_m_s2 NUMERIC(10, 2),
    has_life BOOLEAN NOT NULL,
    atmosphere TEXT,
    surface_temperature_c NUMERIC(10, 2),
    discovery_year INT,
    distance_from_star_km NUMERIC(12, 2),
    CONSTRAINT fk_star_id FOREIGN KEY (star_id) REFERENCES star(star_id)
);

CREATE TABLE moon(
    moon_id SERIAL PRIMARY KEY,
    name VARCHAR(100) NOT NULL UNIQUE,
    planet_id INT NOT NULL,
    radius_km NUMERIC(10, 2) NOT NULL,
    orbital_period_days NUMERIC(10, 2) NOT NULL,
    tidally_locked BOOLEAN NOT NULL,
    composition_moon TEXT,
    distance_from_planet_km NUMERIC(10, 2),
    CONSTRAINT fk_planet_id FOREIGN KEY (planet_id) REFERENCES planet(planet_id)
);

CREATE TABLE nebula(
    nebula_id SERIAL PRIMARY KEY,
    galaxy_id INT NOT NULL,
    nebula_name VARCHAR(100) NOT NULL UNIQUE,
    nebula_type VARCHAR(50) NOT NULL,
    diameter_ly NUMERIC(10,2) NOT NULL,
    distance_ly NUMERIC(10,2) NOT NULL,
    luminosity NUMERIC(15,2),
    composition_nebula TEXT,
    discovery_year INT,
    location_nebula TEXT,
    photo_url TEXT,
    CONSTRAINT fk_galaxy_id_nebula FOREIGN KEY (galaxy_id) REFERENCES galaxy(galaxy_id)
);

CREATE TABLE mission(
    mission_id SERIAL PRIMARY KEY,
    mission_name VARCHAR(100) UNIQUE NOT NULL,
    mission_type VARCHAR(100) NOT NULL,
    agency VARCHAR(100) NOT NULL,
    launch_date DATE NOT NULL,
    budget NUMERIC(15,2),
    description_mission TEXT
);

CREATE TABLE planet_mission(
    planet_mission_id SERIAL PRIMARY KEY,
    planet_id INT NOT NULL,
    mission_id INT NOT NULL,
    planet_mission_name VARCHAR(100) UNIQUE NOT NULL,
    mission_status VARCHAR(50) NOT NULL,
    start_date DATE NOT NULL,
    end_date DATE,
    results TEXT,
    CONSTRAINT fk_planet_mission_planet_id FOREIGN KEY (planet_id) REFERENCES planet(planet_id),
    CONSTRAINT fk_planet_mission_mission_id FOREIGN KEY (mission_id) REFERENCES mission(mission_id)
);

